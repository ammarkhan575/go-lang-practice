package main

import "fmt"

func main() {
	// simple values
	//integer
	fmt.Println(1);
	fmt.Println(1+1);

	//boolean
	fmt.Println(true);
	fmt.Println(false);
	fmt.Println(true && false);
	fmt.Println(true || false);
	fmt.Println(!true);

	//string
	fmt.Println("go" + "lang");

	//float 
	fmt.Println(10.33);
	fmt.Println(1.0/3.0);
}
